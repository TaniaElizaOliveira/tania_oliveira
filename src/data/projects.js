export function getProjectContent(project, locale = "pt") {
  return project.content[locale] ?? project.content.pt;
}

export function getLocalizedProjects(locale = "pt") {
  return projects.map((project) => ({
    ...project,
    ...getProjectContent(project, locale),
  }));
}

export function getProjectBySlug(slug) {
  return projects.find((project) => project.slug === slug);
}

export function getLocalizedProjectBySlug(slug, locale = "pt") {
  const project = getProjectBySlug(slug);

  if (!project) {
    return undefined;
  }

  return {
    ...project,
    ...getProjectContent(project, locale),
  };
}
